nums = {
    0 : 0, 1 : 3, 2 : 3, 3 : 5, 4 : 4, 5 : 4, 6 : 3, 7 : 5, 8 : 5, 9 : 4,
    10 : 3, 11 : 6, 12 : 6, 13 : 8, 14 : 8, 15 : 7, 16 : 7, 17 : 9, 18 : 8, 19 : 8, 
    20 : 6, 30 : 6, 40 : 5, 50 : 5, 60 : 5, 70 : 7, 80 : 6, 90 : 6
}

res = 11 # 1000
for i in range(1, 1000):
    if i >= 100:
        res += nums[i // 100] + 7
        if i % 100 != 0:
            res += 3
    if i % 100 in nums:
        res += nums[i % 100]
    else:
        res += nums[i % 100 - i % 10] + nums[i % 10]

print(res)